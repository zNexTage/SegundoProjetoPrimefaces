package com.br.primefaces.model;

/**
 * @author Christian
 *
 */
public class Telefone  {
	//Atributos
	private int idTelefone;
	private int idCodigoTelefone;
	private String tipoTelefone;
	private String numeroTelefone;
	
	//Getters e Setters 
	/**
	 * @return the idTelefone
	 */
	public int getIdTelefone() {
		return idTelefone;
	}
	/**
	 * @param idTelefone the idTelefone to set
	 */
	public void setIdTelefone(int idTelefone) {
		this.idTelefone = idTelefone;
	}
	/**
	 * @return the idCodigoTelefone
	 */
	public int getIdCodigoTelefone() {
		return idCodigoTelefone;
	}
	/**
	 * @param idCodigoTelefone the idCodigoTelefone to set
	 */
	public void setIdCodigoTelefone(int idCodigoTelefone) {
		this.idCodigoTelefone = idCodigoTelefone;
	}
	/**
	 * @return the tipoTelefone
	 */
	public String getTipoTelefone() {
		return tipoTelefone;
	}
	/**
	 * @param tipoTelefone the tipoTelefone to set
	 */
	public void setTipoTelefone(String tipoTelefone) {
		this.tipoTelefone = tipoTelefone;
	}
	/**
	 * @return the numeroTelefone
	 */
	public String getNumeroTelefone() {
		return numeroTelefone;
	}
	/**
	 * @param numeroTelefone the numeroTelefone to set
	 */
	public void setNumeroTelefone(String numeroTelefone) {
		this.numeroTelefone = numeroTelefone;
	}
	
	
}
